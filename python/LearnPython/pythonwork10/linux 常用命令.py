# ls  查看目录文件
# touch 新建文件  mkdir 新建文件夹  -p递归建立
# rmdir 删除目录 只能删除空目录 非空目录删除不了
# rm 删除 -r 递归删除 -f 强制删除
# mv 移动文件 -f 覆盖前不询问 -i 覆盖前询问 -n不覆盖已经存在的文件
# mv命令在同一个文件夹下移动文件 即重命名功能
# cp 复制 -i 覆盖前询问 -r给出的源文件是目录文件则cp将递归复制到所有子目录和文件
# cat查看文件内容 合并文件
# more查看文件内容
# history 查看历史命令行 -c 删除命令


